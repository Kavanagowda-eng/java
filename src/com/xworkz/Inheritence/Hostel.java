package com.xworkz.Inheritence;

public class Hostel {
 public Hostel() {
            System.out.println("Hostel is the superclass");
        }

        public void provideAccommodation() {
            System.out.println("Hostel provides basic accommodation to guests");
        }

        public void offerMeals() {
            System.out.println("Hostel offers meals to residents");
        }

        public void maintainCleanliness() {
            System.out.println("Hostel maintains cleanliness in rooms and common areas");
        }

        public void provideSecurity() {
            System.out.println("Hostel offers security to ensure safety of the residents");
        }

        public void offerCommonFacilities() {
            System.out.println("Hostel provides common facilities like Wi-Fi and recreation rooms");
        }
    }


